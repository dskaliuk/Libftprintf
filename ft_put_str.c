#include "ft_printf.h"

size_t	ft_put_str(char	*s)
{
	size_t	n;

	n = 0;
	if (s)
	{
		while (s[n])
		{
			write (1, &s[n], 1);
			n++;
		}
		free (s);
	}
	return (n);
}
